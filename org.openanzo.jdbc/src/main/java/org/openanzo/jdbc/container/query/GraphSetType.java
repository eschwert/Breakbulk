package org.openanzo.jdbc.container.query;

/**
 * Graph set type
 * 
 * @author Matthew Roy ( <a href="mailto:mroy@cambridgesemantics.com">mroy@cambridgesemantics.com</a>)
 * 
 */
public enum GraphSetType {
    /** Set of all graphs, data and metadata */
    ALL_GRAPHS,
    /** Set of all data graphs only */
    ALL_NAMED_GRAPHS,
    /** Set of all metadata graphs only */
    ALL_METADATA_GRAPHS,
    /** A listed set of graphs */
    LISTED
}
